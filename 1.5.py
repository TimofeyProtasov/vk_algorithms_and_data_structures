# Ближайшие соседи
#
# Дан отсортированный массив положительных целых чисел.
# Необходимо найти такую пару соседних элементов, у которых абсолютная разница минимальна.
#
# Input data format
# В первой строке задаётся число N — размер массива (количество элементов).
# Во второй строке вводится массив из N положительных целых чисел, разделённых пробелами.
#
# Output data format
# Программа должна вывести два числа — значения пары соседних элементов с наименьшей абсолютной разницей,
# разделённые пробелом.
# Если таких пар несколько, вывести первую найденную.
#
# Example 1
# Input data
# 4
# 1 3 4 11
#
# Output data
# 3 4

_ = input()
arr = list(map(int, input().split()))

min_diff = arr[1] - arr[0]
best_pair = (arr[0], arr[1])

for i in range(2, len(arr)):
    diff = arr[i] - arr[i - 1]
    if diff < min_diff:
        min_diff = diff
        best_pair = (arr[i - 1], arr[i])

print(*best_pair)